export function pipe(
  a: unknown,
  // eslint-disable-next-line @typescript-eslint/ban-types
  ab?: Function,
  // eslint-disable-next-line @typescript-eslint/ban-types
  bc?: Function,
  // eslint-disable-next-line @typescript-eslint/ban-types
  cd?: Function,
  // eslint-disable-next-line @typescript-eslint/ban-types
  de?: Function,
  // eslint-disable-next-line @typescript-eslint/ban-types
  ef?: Function,
  // eslint-disable-next-line @typescript-eslint/ban-types
  fg?: Function,
  // eslint-disable-next-line @typescript-eslint/ban-types
  gh?: Function,
  // eslint-disable-next-line @typescript-eslint/ban-types
  hi?: Function
): unknown {
  switch (arguments.length) {
    case 1:
      return a
    case 2:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return ab!(a)
    case 3:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return bc!(ab!(a))
    case 4:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return cd!(bc!(ab!(a)))
    case 5:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return de!(cd!(bc!(ab!(a))))
    case 6:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return ef!(de!(cd!(bc!(ab!(a)))))
    case 7:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return fg!(ef!(de!(cd!(bc!(ab!(a))))))
    case 8:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return gh!(fg!(ef!(de!(cd!(bc!(ab!(a)))))))
    case 9:
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return, @typescript-eslint/no-non-null-assertion
      return hi!(gh!(fg!(ef!(de!(cd!(bc!(ab!(a))))))))
    default: {
      // eslint-disable-next-line @typescript-eslint/no-unsafe-assignment, prefer-rest-params
      let ret = arguments[0]
      for (let i = 1; i < arguments.length; i++) {
        // eslint-disable-next-line @typescript-eslint/no-unsafe-assignment, prefer-rest-params, @typescript-eslint/no-unsafe-call
        ret = arguments[i](ret)
      }
      // eslint-disable-next-line @typescript-eslint/no-unsafe-return
      return ret
    }
  }
}
